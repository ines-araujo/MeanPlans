/* Convert all units to standart */

/* Weight: Convert to grams */

module.exports = function MiligTog(value){
    return value * 1000;
}

module.exports = function KilogTog(value){
    return value / 1000;
}

module.exports = function OzTog(value){
    return value * 28.35;
}

module.exports = function TbspTog(value){ // Weight and volume
    return value * 17.07;
}

module.exports = function TspTog(value){ // Weight and volume
    return value * 5.69;
}

module.exports = function PinchTog(value){
    return value * 5.69;
}

/* Volume: Convert to litre */

module.exports = function KilogTog(value){
    return value / 1000;
}

